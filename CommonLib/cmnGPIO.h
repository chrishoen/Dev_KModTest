/*==============================================================================
This provides a wrapper for the gpio facilities.
==============================================================================*/

//******************************************************************************
//******************************************************************************
//******************************************************************************

namespace Cmn
{

//******************************************************************************
//******************************************************************************
//******************************************************************************
// This class provides an interface to some gpio bits.
// GND    : PIN25
// GPIO5  : PIN29 OUT : A 
// GPIO6  : PIN31 IN  : B
// GPIO13 : PIN33

class GPIO
{
public:

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Constants.

   static const int cGpioA = 5;
   static const int cGpioB = 6;

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Members.

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Constructor.
   GPIO();

   // Device initialize and finalize.
   void initialize();
   void finalize();

   //***************************************************************************
   //***************************************************************************
   //***************************************************************************
   // Methods.

   // Write to the digital output for gpio A.
   void writeA(bool aValue);

   // Read from the digital output for gpio B.
   bool readB();

};

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Global singular instance.

#ifdef _CMNGPIO_CPP_
          GPIO gGPIO;
#else
   extern GPIO gGPIO;
#endif

//******************************************************************************
//******************************************************************************
//******************************************************************************
}//namespace

